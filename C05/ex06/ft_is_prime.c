int	ft_is_prime(int nb)
{
	int number;

	number = 2;
	if (nb < 2)
	{
		return (0);
	}
	while (number <= nb / 2)
	{
		if (nb % number == 0)
		{
			return (0);
		}
		else
		{
			number++;
		}
	}
	return (1);
}
