int		is_digit(char c)
{
	return ((c >= '0') && (c <= '9'));
}

int		ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (!(is_digit(str[i])))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
