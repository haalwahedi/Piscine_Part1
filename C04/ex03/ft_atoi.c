int		ft_atoi(char *str)
{
	int i;
	int sign;

	i = 0;
	sign = 1;
	while (*str == ' ')
		str++;
	while ((*str == '-') || (*str == '+'))
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str)
	{
		if ((*str >= '0') && (*str <= '9'))
			i = (i * 10) + (*str - '0');
		else
			break ;
		str++;
	}
	return (i * sign);
}
